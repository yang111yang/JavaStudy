package com.liu.server;
/**
 * web.xml中servlet的实体类
 * @author liujy
 *
 */
public class Entity {
	
	private String name;
	private String clz;
	
	public Entity() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getClz() {
		return clz;
	}

	public void setClz(String clz) {
		this.clz = clz;
	}

	
}
